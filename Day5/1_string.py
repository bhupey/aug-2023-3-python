# Strings are immutable datypes in python
# they are also sequential like list so indexing and slicing are possible in string
# Creating empty string
a = str() #empty string
a = "" # empty string


# creating non empty strings
message = "hello world"    #using double quote
message = 'hello world'    # using single quote
message = """hello world""" # using triple quote


#Escape Characters
# \", \', \n, \t etc are the escape characters
message = "Hello \n World"
print(message)
message = "Hello \t World"
print(message)